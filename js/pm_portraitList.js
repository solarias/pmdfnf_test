function indexPortrait(name, wantType) {
	var arr = portraitList;
	
	for (var i=0;i<arr.length;i++) {
		if (arr[i][0] == name) {
			switch (wantType) {
				case "name":
					return arr[i][1];
				case "image":
					return arr[i][2];
			}
		}
	}
}

